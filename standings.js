function updateStandings(season) {
    activeData = [];

    const dropDown = document.getElementById('seasonDropdown');
    const subStandings = document.getElementById('subStandings');

    if (season === 1) {
        activeData = season1Stats;
        dropDown.textContent = 'Season 1';
        subStandings.textContent = 'Season #1 Standings';
    } else if (season === 2) {
        dropDown.textContent = 'Season 2';
        subStandings.textContent = 'Season #2 Standings';
        activeData = season2Stats;
    } else if (season == 3) {
        dropDown.textContent = 'Season 3';
        subStandings.textContent = 'Season #3 Starting Soon!';
    }

    const table = document.getElementById('standingsTable');
    const thead = table.querySelector('thead');
    const tbody = table.querySelector('tbody');

    // Clear existing headers and rows
    thead.innerHTML = '';
    tbody.innerHTML = '';

    // Set new headers
    const headerRow = document.createElement('tr');
    headerRow.innerHTML = `
        <th>Position</th>
        <th>Square</th>
        <th>Wins</th>
        <th>Losses</th>
        <th>Draws</th>
    `;
    thead.appendChild(headerRow);

    // Sort teams by points descending
    // Sort teams by lowest losses, then highest wins
    const sortedStandings = [...activeData].sort((a, b) => {
        if (a.Losses !== b.Losses) {
            return a.Losses - b.Losses; // Ascending order of losses
        }
        return b.Wins - a.Wins; // Descending order of wins
    });
    
    sortedStandings.forEach((team, index) => {
        const row = document.createElement('tr');
        row.innerHTML = `
            <td>${index + 1}</td>
            <td>${team.Square}</td>
            <td>${team.Wins}</td>
            <td>${team.Losses}</td>
            <td>${team.Draws}</td>
        `;
        tbody.appendChild(row);
    });
}

document.addEventListener('DOMContentLoaded', function () {
    updateStandings(2); // Initialize Season 2 standings
});
